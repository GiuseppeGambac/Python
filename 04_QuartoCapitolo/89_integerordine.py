def ordinamento(numero):
    if numero == 1:
        return "primo"
    elif numero ==2:
        return "secondo"
    elif numero ==3:
        return "terzo"
    elif numero ==4:
        return "quarto"
    else:
        return  "numero fuori range"
    
    
    
    
print(ordinamento(2))
print(ordinamento(50))